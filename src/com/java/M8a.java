public class M8a 
{
	public static void main(String[] args) 
	{
		byte mn = 27;
		final int xy = 98;
		final byte by = 67;
		switch(mn)
		{
		case xy:
			System.out.println("Final int value");
		case by:
			System.out.println("Final Byte value");
		case 28:
			System.out.println("int 27 value");
		default:
			System.out.println("By Default value");
		
		}

	}

}
