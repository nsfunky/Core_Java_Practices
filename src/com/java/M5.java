package com.java;

// using BITWISE NOT(~) - AND(&) - OR(|) - XOR(^) Operator

public class M5 
{
	public static void main(String[] args) 
	{
		int i=-5;
		int j = ~i;
		System.out.println(j);
		/*
		 * Bitwise Complement Operator (~) performs a bitwise negation
		 * Of an Integer  value. Each bit in a number will be toggled.		
		 */
		int k=4;
		int l=~k;
		System.out.println(l);
		
	}
// TRICK --> just Change the sign of the given number & add  -1 to that.
}
